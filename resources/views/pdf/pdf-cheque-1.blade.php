										<li>Presentar <strong>AL CEDIDO</strong>, las Facturas componentes de la nota oferta y que deben estar debidamente endosadas a favor <strong>DEL FACTOR</strong>, con el texto al dorso de las mismas <strong>“ENDOSO [CON RECURSO] Y SIN PROTESTO A LA ORDEN DE ____”</strong></li>
										<li>Gestionar ante <strong>EL CEDIDO</strong> la conformación de las facturas a negociar bajo Factoring.</li>
										<div style="page-break-before:always; padding-top: 40px;">&nbsp;</div> 
										<li>A mantener en custodia las Facturas Conformadas, las que estarán permanentes a disposición <strong>DEL FACTOR</strong>, actuando para el efecto en carácter de Depositario <strong>DEL FACTOR</strong>. Deberá proceder a su entrega <strong>AL FACTOR</strong> dentro de las 24 horas. Una vez que tales facturas hayan sido pagadas por <strong>EL CEDIDO</strong> y/o por <strong>EL CEDENTE, EL CEDENTE</strong> terminará su condición de Depositario de tales facturas.</li>
										<li>En caso que <strong>EL CEDIDO</strong>, por error u omisión pague directamente <strong>AL CEDENTE</strong> el monto de los documentos negociados bajo factoring, EL CEDENTE deberá reembolsar o transferir <strong>AL FACTOR</strong>, tal monto y por lo cual <strong>EL CEDENTE</strong> autoriza <strong>AL FACTOR</strong> a debitar en su cuenta o cuentas que mantiene en <strong>EL FACTOR</strong>.</li>
