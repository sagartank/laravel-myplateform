									<p><strong>SEGUNDA: EL CEDENTE</strong>, acepta en forma irrevocable que los derechos de cobro de los documentos de crédito conformados y negociados bajo operación de Factoring, corresponden única y exclusivamente <strong>AL FACTOR</strong> y deben ser pagados a éste por <strong>EL CEDIDO</strong> a su vencimiento.</p>
